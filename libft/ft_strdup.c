#include "libft.h"

char	*ft_strdup(char *src)
{
	int		i;
	int		srclen;
	char	*str;

	if (!src)
		return (NULL);
	srclen = ft_strlen(src);
	if (!(str = (char *)malloc(sizeof(*str) * (srclen + 1))))
		return (NULL);
	i = 0;
	while (src[i])
	{
		str[i] = src[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
