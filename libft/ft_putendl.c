#include "libft.h"

void	ft_putendl(const char *s)
{
	int cpt;

	if (s)
	{
		cpt = 0;
		while (s[cpt])
		{
			ft_putchar(s[cpt]);
			cpt += 1;
		}
		ft_putchar('\n');
	}
}
