#include "libft.h"

void	ft_putstr_fd(char const *s, int fd)
{
	int cpt;

	if (s)
	{
		cpt = 0;
		while (s[cpt])
		{
			ft_putchar_fd(s[cpt], fd);
			cpt += 1;
		}
	}
}
