#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	int		i;

	if (s && f)
	{
		i = 0;
		while (s[i])
		{
			f((int)i, &s[i]);
			i++;
		}
	}
}
